







def rotate_and_expand(point):
    x = point[0]
    y = point[1]
    new_x = 1.1 * y
    new_y = -1.1 * x
    return (new_x, new_y)

def rotate_and_expand_list(point):
    times = 1
    points = []
    if times < 50:
        x = point[0]
        y = point[1]
        new_x = 1.1 * y
        new_y = -1.1 * x
        times += 1
        append.points
    return (points)





